    private String dateTimeformat(String old){
        String newDateTime = "";
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            Date date = sdf.parse(old);
            sdf = new SimpleDateFormat("yyyyMMddHHmmss");
            newDateTime = sdf.format(date);
        } catch (Exception e) {
            e.printStackTrace();
        }
        return newDateTime;
    }

